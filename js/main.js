let currentLang = localStorage.getItem("paf-lang") || "es";

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem("paf-lang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const text = translations[lang][key];
    if (text !== undefined) el.textContent = text;
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle("active", active);
    btn.setAttribute("aria-pressed", active);
  });
}

/* Header scroll */
const header = document.getElementById("header");
window.addEventListener("scroll", () => {
  header.classList.toggle("scrolled", window.scrollY > 40);
}, { passive: true });

/* Mobile menu */
const menuToggle = document.querySelector(".menu-toggle");
const mobileNav = document.getElementById("mobile-nav");

menuToggle.addEventListener("click", () => {
  const open = mobileNav.classList.toggle("open");
  menuToggle.classList.toggle("open", open);
  menuToggle.setAttribute("aria-expanded", open);
  mobileNav.hidden = !open;
  document.body.style.overflow = open ? "hidden" : "";
});

mobileNav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", (e) => {
    if (link.hasAttribute("data-login")) return;
    mobileNav.classList.remove("open");
    menuToggle.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    mobileNav.hidden = true;
    document.body.style.overflow = "";
  });
});

/* Language switch */
document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
});

/* Scroll reveal */
const revealEls = document.querySelectorAll(
  ".section-grid, .section-header-center, .pillars, .approach-card, .tech-grid, .contact-block"
);
revealEls.forEach((el) => el.classList.add("reveal"));

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
);

revealEls.forEach((el) => observer.observe(el));

/* Login fade transition */
document.querySelectorAll("[data-login]").forEach((link) => {
  link.addEventListener("click", (e) => {
    e.preventDefault();
    document.body.classList.add("page-exit");
    setTimeout(() => {
      window.location.href = link.getAttribute("href");
    }, 450);
  });
});

/* Init */
setLanguage(currentLang);
